let currentImageIndex = 0;

// Function to reveal or hide ASCII art
function animateAsciiArt(asciiArt, hide = false, onComplete) {
  const chars = asciiArt.split('');
  let display = hide
    ? chars.slice() // start with full art if hiding
    : chars.map(c => (c === '\n' ? '\n' : ' ')); // start empty if revealing

  const indices = chars
    .map((c, i) => i)
    .filter(i => chars[i] !== '\n');

  // Shuffle helper
  function shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]];
    }
  }

  shuffle(indices);

  const batchSize = 20;
  const totalDuration = 3; // seconds
  const steps = Math.ceil(indices.length / batchSize);
  const interval = (totalDuration * 1000) / steps;

  let current = 0;

  function step() {
    for (let i = 0; i < batchSize && current < indices.length; i++, current++) {
      const idx = indices[current];
      display[idx] = hide ? ' ' : chars[idx]; // hide => space, reveal => char
    }
    container.textContent = display.join('');

    if (current < indices.length) {
      setTimeout(step, interval);
    } else {
      onComplete();
    }
  }

  step();
}

// Rotation logic with hide
function startRotation() {
  const ascii = asciiImages[currentImageIndex];
  animateProgressBar(3000 );

  // Reveal
  animateAsciiArt(ascii, false, () => {
    // Wait 7 seconds
    setTimeout(() => {
      // Hide
      animateAsciiArt(ascii, true, () => {
        // Move to next image and repeat
        currentImageIndex = (currentImageIndex + 1) % asciiImages.length;
        startRotation();
      });
    }, 7000);
  });
}

// Start!
startRotation();

